# TAB 2 — CLASSIFICATION
# ----------------------------------------------------------------------------
with tab_cls:
    st.markdown('<p class="section-header">Random Forest — Expense Category Classifier</p>',
                unsafe_allow_html=True)

    model_path = _path("models", "rf_model.pkl")
    model_exists = os.path.exists(model_path) and JOBLIB_OK

    if model_exists:
        try:
            rf_model = joblib.load(model_path)

            st.success("Model loaded successfully from `models/rf_model.pkl`")

            # -- Category mapping (from preprocess.py contract) --------------
            EXP_TYPES = ["Bills", "Entertainment", "Food", "Fuel", "Grocery", "Travel"]
            CITIES = ["Ahmedabad, India", "Bengaluru, India", "Chennai, India",
                      "Delhi, India", "Greater Mumbai, India", "Kolkata, India"]
            CARD_TYPES = ["Gold", "Platinum", "Silver", "Signature"]
            GENDERS = ["F", "M"]

            st.markdown("#### Predict Expense Category")
            st.caption("Enter transaction details to classify the expense type.")

            c1, c2, c3 = st.columns(3)
            with c1:
                amount = st.number_input("Amount (₹)", min_value=1.0, value=5000.0, step=100.0)
                city = st.selectbox("City", CITIES)
            with c2:
                card = st.selectbox("Card Type", CARD_TYPES)
                gender = st.selectbox("Gender", GENDERS)
            with c3:
                month = st.slider("Month", 1, 12, 6)
                dow = st.slider("Day of Week (0=Mon)", 0, 6, 2)
                week = st.slider("Week Number", 1, 52, 26)

            if st.button("🔍 Predict Category", type="primary"):
                # Build feature vector matching categorise.py training columns
                city_enc = CITIES.index(city) if city in CITIES else 0
                card_enc = CARD_TYPES.index(card) if card in CARD_TYPES else 0
                gen_enc = 0 if gender == "F" else 1

                # amount_norm: rough estimate using train max (₹1,000,000 assumed)
                amount_norm = min(amount / 1_000_000.0, 1.0)

                features = pd.DataFrame([[
                    amount_norm, dow, month, week,
                    card_enc, gen_enc, city_enc
                ]], columns=[
                    "amount_norm", "day_of_week", "month", "week_number",
                    "card_type_encoded", "gender_encoded", "city_encoded"
                ])

                pred_encoded = rf_model.predict(features)[0]
                pred_label = EXP_TYPES[pred_encoded] if pred_encoded < len(EXP_TYPES) else str(pred_encoded)

                try:
                    proba = rf_model.predict_proba(features)[0]
                    st.success(f"**Predicted category: {pred_label}**")
                    st.markdown("**Confidence scores:**")
                    proba_df = pd.DataFrame({
                        "Category": EXP_TYPES[:len(proba)],
                        "Confidence": [f"{p*100:.1f}%" for p in proba]
                    })
                    st.dataframe(proba_df, use_container_width=True, hide_index=True)
                except Exception:
                    st.success(f"**Predicted category: {pred_label}**")

            # -- Feature importance -----------------------------------------
            st.divider()
            st.markdown("#### Feature Importance")
            feat_names = ["amount_norm", "day_of_week", "month", "week_number",
                          "card_type_encoded", "gender_encoded", "city_encoded"]
            try:
                importances = rf_model.feature_importances_
                fi_df = pd.DataFrame({
                    "Feature": feat_names[:len(importances)],
                    "Importance": importances
                }).sort_values("Importance", ascending=False)

                if PLOTLY_OK:
                    fig = px.bar(fi_df, x="Importance", y="Feature",
                                 orientation="h",
                                 color="Importance",
                                 color_continuous_scale="Blues")
                    fig.update_layout(height=300, margin=dict(t=10, b=10),
                                      coloraxis_showscale=False)
                    st.plotly_chart(fig, use_container_width=True)
                else:
                    st.bar_chart(fi_df.set_index("Feature"))
            except Exception as e:
                st.info(f"Feature importance not available: {e}")

        except Exception as e:
            st.error(f"Error loading model: {e}")

    else:
        st.info(
            "**Model not yet available.**\n\n"
            "To generate it, run `python src/categorise.py`.\n\n"
            "This will train the Random Forest classifier and save it to `models/rf_model.pkl`."
        )
        # Show what the model should do
        st.markdown("#### What this tab will show")
        st.markdown("""
        - **Transaction input form** — enter amount, city, card type, gender, date info
        - **Predicted expense category** — Bills / Entertainment / Food / Fuel / Grocery / Travel
        - **Confidence scores** — probability per class from the Random Forest
        - **Feature importance chart** — which features drive the classification most
        """)

        if train_df is not None:
            st.markdown("#### Category distribution in training data")
            cat_dist = train_df["exp_type"].value_counts().reset_index()
            cat_dist.columns = ["Category", "Count"]
            if PLOTLY_OK:
                fig = px.bar(cat_dist, x="Category", y="Count",
                             color="Category",
                             color_discrete_sequence=px.colors.qualitative.Set2)
                fig.update_layout(showlegend=False, height=280,
                                  margin=dict(t=10, b=10))
                st.plotly_chart(fig, use_container_width=True)
            else:
                st.bar_chart(cat_dist.set_index("Category"))


