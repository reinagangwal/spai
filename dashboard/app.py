def _path(*parts):
    return os.path.join(REPO_ROOT, *parts)


def _read_csv(rel_path: str) -> pd.DataFrame | None:
    full = _path(rel_path)
    if not os.path.exists(full):
        return None
    try:
        return pd.read_csv(full)
    except Exception:
        return None


