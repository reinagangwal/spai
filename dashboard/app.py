# TAB 4 — FORECAST
# ----------------------------------------------------------------------------
with tab_forecast:
    st.markdown('<p class="section-header">LSTM Forecasting — 7-Week Spend Predictions</p>',
                unsafe_allow_html=True)

    if forecast_df is not None:
        forecast_df["date"] = pd.to_datetime(forecast_df["date"])
        categories = [c for c in forecast_df.columns if c != "date"]

        # -- Forecast KPIs ----------------------------------------------------
        st.markdown("#### 7-week forecast (normalized spend per category)")
        cols = st.columns(len(categories))
        for i, cat in enumerate(categories):
            trend = forecast_df[cat].iloc[-1] - forecast_df[cat].iloc[0]
            arrow = "↑" if trend > 0 else "↓"
            cols[i].metric(
                label=cat,
                value=f"{forecast_df[cat].mean():.3f}",
                delta=f"{arrow} {abs(trend):.3f}",
            )

        st.divider()

        # -- Line chart -------------------------------------------------------
        st.markdown("#### Forecast trend by category")

        if PLOTLY_OK:
            fig = go.Figure()
            colors = px.colors.qualitative.Set2
            for i, cat in enumerate(categories):
                fig.add_trace(go.Scatter(
                    x=forecast_df["date"],
                    y=forecast_df[cat],
                    mode="lines+markers",
                    name=cat,
                    line=dict(color=colors[i % len(colors)], width=2),
                    marker=dict(size=6),
                ))
            fig.update_layout(
                height=380,
                margin=dict(t=10, b=10),
                xaxis_title="Date",
                yaxis_title="Predicted Amount (normalized)",
                legend=dict(orientation="h", yanchor="bottom", y=1.02),
            )
            st.plotly_chart(fig, use_container_width=True)
        else:
            st.line_chart(forecast_df.set_index("date")[categories])

        st.caption(
            "⚠️ Note: Values are in normalized scale (amount_norm). "
            "Multiply by max training spend (~₹1,000,000) for approximate real amounts."
        )

        # -- Raw table --------------------------------------------------------
        with st.expander("View raw forecast data"):
            show_df = forecast_df.copy()
            show_df["date"] = show_df["date"].dt.strftime("%Y-%m-%d")
            st.dataframe(show_df, use_container_width=True, hide_index=True)

    else:
        st.warning("Run `python src/forecast.py` to generate `outputs/forecast.csv`.")

    # -- Training loss plot ---------------------------------------------------
    st.divider()
    st.markdown("#### LSTM Training Loss Curve")
    loss_path = _path("outputs", "training_loss.png")
    if os.path.exists(loss_path):
        st.image(loss_path, caption="Train vs Validation Loss over epochs", use_column_width=True)
        st.success(
            "Train and validation loss track closely together — "
            "no significant overfitting detected."
        )
    else:
        st.info("Training loss plot will appear here once `forecast.py` has been run.")

